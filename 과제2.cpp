#include <iostream>

int main()
{
	float num1 = 3;
	float num2 = 5;




	std::cout << "First number>> 3 "<<'\n';
	std::cout << "Second number>> 5"<<'\n';


	std::cout << "=================계산 결과====================="<<'\n';
	std::cout << "3+5 = "<< (num1+num2) <<'\n';
	std::cout << "3-5 = "<< (num1-num2) <<'\n';
	std::cout << "3*5 = "<< (num1*num2) <<'\n';
	std::cout << "3/5 = "<< (num1/num2) <<'\n';
	
}