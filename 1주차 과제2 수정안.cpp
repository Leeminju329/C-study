#include<iostream>
using namespace std;

int main()
{
	float num1, num2;

	cout << "First number>> ";
	cin >> num1;

	cout << "Second number>> ";
	cin >> num2;

	cout << "=========계산 결과==========\n";


	cout << num1 << "+" << num2 << "=" << (num1 + num2) << '\n';
	cout << num1 << "-" << num2 << "=" << (num1 - num2) << '\n';
	cout << num1 << "*" << num2 << "=" << (num1 * num2) << '\n';
	cout << num1 << "/" << num2 << "=" << (num1 / num2) << '\n';

	

	

}